        doc.add(paragraph2);
        PdfPTable tb1 = new PdfPTable(5);
        tb1.addCell("Name");
        tb1.addCell("Description");
        tb1.addCell("Rate");
        tb1.addCell("Quantity");
        tb1.addCell("Sub Total");
        for(int i=0;i<jTable1.getRowCount();i++)
        {  
              String n = jTable1.getValueAt(i,0).toString();
              String d = jTable1.getValueAt(i,1).toString();
              String r = jTable1.getValueAt(i,2).toString();
              String q = jTable1.getValueAt(i,3).toString();
              String s = jTable1.getValueAt(i,4).toString();
              tb1.addCell(n);
               tb1.addCell(d);
                tb1.addCell(r);
                 tb1.addCell(q);
                tb1.addCell(s);
                 
        } 
        doc.add(tb1);
        Paragraph paragraph3 = new Paragraph("\n                Total: "+jTextField10.getText()+"\n                 Paid Amount: "+jTextField11.getText()+"\n               Returned Amount: "+jTextField12.getText()+"\n\n\n                                  Thank you for visting !Please come Again.\n                                 ");
