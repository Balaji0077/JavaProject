    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField6.getText().equals("")||jTextField7.getText().equals("")||jTextField8.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Enter the Required Fields");
        }
        else
        {    
        float price = Float.parseFloat(jTextField7.getText());
        float quantity = Float.parseFloat(jTextField8.getText());
        float subtotal = price * quantity;
        DefaultTableModel model= (DefaultTableModel)jTable1.getModel();
        model.addRow(new Object[]{jTextField6.getText(),jTextField9.getText(),price,quantity,subtotal});
        finalTotal = finalTotal+subtotal;
        String finalTotal1 = String.valueOf(Math.floor(finalTotal));
        clearFields();
        jTextField10.setText(finalTotal1);
        jTextField10.setEditable(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
